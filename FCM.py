def sigmoid(x, lambd):
    return 1 / (1 + np.exp(-lambd * x))

def run_fcm(W, A_init, lambd, steps, epsilon):
    history = [A_init]
    current_state = A_init
    
    for _ in range(steps):
        # çŸ©é™£é‹ç®—
        influence = np.dot(current_state, W)
        
        # â˜…â˜…â˜… é—œéµä¿®æ­£ï¼šåŠ å…¥æ…£æ€§ (Self-memory) â˜…â˜…â˜…
        # ä¸‹ä¸€ç‹€æ…‹ = èˆŠç‹€æ…‹ + æ–°å½±éŸ¿ (è®“æ›²ç·šå¹³æ»‘åŒ–ï¼Œä¸æœƒç¬é–“è·³è®Š)
        # é€™è£¡å‡è¨­ç•¶å‰ç‹€æ…‹æœ‰ 0.5 çš„ä¿ç•™ç‡ï¼Œè®“è®ŠåŒ–æ›´è‡ªç„¶
        new_val = sigmoid(influence, lambd)
        
        # å¹³æ»‘å…¬å¼ï¼šNew = 0.3 * Old + 0.7 * Calculated
        # é€™èƒ½è§£æ±ºã€Œç›´ç·šã€å•é¡Œï¼Œè®“å®ƒè®Šæˆæ›²ç·š
        next_state = 0.3 * current_state + 0.7 * new_val
        
        history.append(next_state)
        current_state = next_state
        
    return np.array(history)

